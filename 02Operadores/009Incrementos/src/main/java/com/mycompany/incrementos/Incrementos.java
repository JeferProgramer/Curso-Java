package com.mycompany.incrementos;

public class Incrementos {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int x = 10;
        
        //Incremento en 1 
        //10 + 1 = 11
        x++;
        System.out.println(x);
        
        //Decremento en 1
        x--;
        System.out.println(x);
    }
    
}
